function resolveStandardNation(nationality: string) {
	const iso3166Lookup = require('iso-3166-1');

	try {
		return iso3166Lookup.whereCountry(nationality).alpha2;
	}
	catch {
		// log? display message?
	}
}

function resolveNonStandardNation(nationality: string) {
	switch (nationality) {
		case 'England':
			return 'GB-ENG';
		case 'Northern Ireland':
			return 'GB-NIR';
		case 'Scotland':
			return 'GB-SCT';
		case 'Wales':
			return 'GB-WLS';
		case 'Catalonia':
			return 'ES-CT';
		case 'Galacia':
			return 'ES-GA';
    }
}
